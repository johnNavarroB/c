void	ft_rev_int_tab(int *tab, int size)
{
	int	index;
	int	auxiliar;

	index = 0;
	while (index != (size / 2))
	{
		auxiliar = tab[index];
		tab[index] = tab[size - (index + 1)];
		tab[size - (index + 1)] = auxiliar;
		index ++;
	}
}