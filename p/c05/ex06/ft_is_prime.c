int	ft_is_prime(int nb)
{
	int index;

	index = 2;
	while (index <= nb / 2)
	{
		if (nb % index == 0)
		{
			return (0);
		}
		index ++;
	}
	if (nb < 2)
	{
		return (0);
	}
	return (1);
}